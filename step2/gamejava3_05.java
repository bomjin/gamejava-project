package gamejava3;

public class gamejava3_05 {

	public static void main(String[] args) {
		
		int num=2;
		
		switch(num) {
		case 1:
			System.out.println("결과는 1");
			break;
		case 2:
			System.out.println("결과는 2");
		case 3:
			System.out.println("결과는 3");
			break;
			default:
				System.out.println("결과는 1,2,3,이 아닙니다.");
				break;
			
		}
		
	}

}
